const changeLanguage = (lang) => {
    for (let key in translations[lang]) {
        if (key.endsWith("List")) {
            const listElement = document.querySelector(`[data-translate-key=${key}]`);
            listElement.innerHTML = "";  // Clear the current list items
            translations[lang][key].forEach(item => {
                const li = document.createElement('li');
                li.innerHTML = item;
                listElement.appendChild(li);
            });
        } else {
            const element = document.querySelector(`[data-translate-key=${key}]`);
            element.innerHTML = translations[lang][key];
        }
    }
}

const updateActiveLanguage = (lang) => {
    document.querySelectorAll('.lang').forEach(link => {
        link.classList.remove('active'); // Remove active class from all language links
    });
    document.querySelector(`[data-lang=${lang}]`).classList.add('active'); // Add active class to selected language link
}

// Default language
changeLanguage('en');
updateActiveLanguage('en');

// Event delegation for language switch
document.querySelector("#languageLinks").addEventListener('click', function(e) {
    if (e.target.classList.contains('lang')) {
        const selectedLang = e.target.getAttribute('data-lang');
        changeLanguage(selectedLang);
        updateActiveLanguage(selectedLang);
    }
});
